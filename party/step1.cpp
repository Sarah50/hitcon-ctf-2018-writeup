typedef unsigned long long ll;
const unsigned int P = 400000007u;
unordered_map<ll, bool> occurred;
ll h[P];

const int N = 538;
const char STR[] = "oidn1be8!kasgm2q5jwplz7rhvy094xfu6t3c";

ll S = 0;
int a[10];
bool b[40];

void print_bin(ll bin)
{
	for (int i = 0; i < 37; i++)
		printf("%llu", (bin >> i) & 1);
	puts("");
}

void dfs(int t, int p, ll vset)
{
	if (t == 5)
	{
		S++;
		for (ll tmp = vset; tmp;)
		{
			ll lowbit = tmp & -tmp;
			ll core = vset - lowbit;
			tmp -= lowbit;
			if (((h[core % P] |= lowbit) | core) == (1ll << 37) - 1)
			{
				if (occurred[core])
					continue;
				occurred[core] = true;
				print_bin(core);
			}
		}
		if (S % 100000000 == 0)
			cerr << S << endl;
		return;
	}
	for (int i = p, j; i < N; i++)
	{
		if (b[DATA[i][0]] || b[DATA[i][1]] || b[DATA[i][2]])
			continue;
		a[t] = i;
		for (j = i + 1; j < N && DATA[i][0] == DATA[j][0]; j++);
		b[DATA[i][0]] = b[DATA[i][1]] = b[DATA[i][2]] = 1;
		dfs(t + 1, j, vset | (1ll << DATA[i][0]) | (1ll << DATA[i][1]) | (1ll << DATA[i][2]));
		b[DATA[i][0]] = b[DATA[i][1]] = b[DATA[i][2]] = 0;
	}
}

